public class Futoshiki {
    public class FTuple {
        private int x;
        private int y;
        
        public FTuple(int x, int y) {
            this.x = x;
            this.y = y;
        }

        public int getX() {
            return x;
        }

        public int getY() {
            return y;
        }
    }
}
